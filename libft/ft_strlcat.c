#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	ret;

	j = 0;
	i = ft_strlen(dst);
	if (dstsize > i)
		ret = i + ft_strlen(src);
	else
		ret = ft_strlen(src) + dstsize;
	while (src[j] != '\0' && i + 1 < dstsize)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (ret);
}
